#!/usr/bin/env node
// Regenerates manifest.json and the README catalog table from companies/*.
import { readFileSync, writeFileSync, readdirSync, existsSync, statSync } from 'node:fs';
import { join, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import { parse as parseYaml } from 'yaml';

const ROOT = resolve(fileURLToPath(new URL('..', import.meta.url)));
const COMPANIES_DIR = join(ROOT, 'companies');

const listDirs = (dir) =>
	existsSync(dir) ? readdirSync(dir).filter((d) => statSync(join(dir, d)).isDirectory()) : [];

const fm = (file) => {
	const m = /^---\r?\n([\s\S]*?)\r?\n---/.exec(readFileSync(file, 'utf8'));
	return m ? parseYaml(m[1]) : null;
};

const companies = listDirs(COMPANIES_DIR).map((slug) => {
	const dir = join(COMPANIES_DIR, slug);
	const company = fm(join(dir, 'COMPANY.md')) ?? {};
	const ew = parseYaml(readFileSync(join(dir, '.everworks.yaml'), 'utf8')) ?? {};
	const counts = {
		agents: listDirs(join(dir, 'agents')).length,
		teams: listDirs(join(dir, 'teams')).length,
		skills: listDirs(join(dir, 'skills')).length,
		projects: listDirs(join(dir, 'projects')).length,
	};
	const entry = {
		slug,
		path: `companies/${slug}`,
		name: company.name ?? slug,
		description: company.description ?? '',
		category: ew.catalog?.category ?? 'general',
		...counts,
		avatarIcon: ew.catalog?.avatarIcon ?? 'building-2',
		tags: ew.catalog?.tags ?? [],
	};
	if (ew.catalog?.featured) entry.featured = true;
	return entry;
});

companies.sort((a, b) => (b.featured ? 1 : 0) - (a.featured ? 1 : 0) || a.slug.localeCompare(b.slug));

const manifest = {
	$schema: './schema/orgs-manifest.schema.json',
	version: 1,
	generatedBy: 'scripts/build-manifest.mjs',
	companies,
};
writeFileSync(join(ROOT, 'manifest.json'), JSON.stringify(manifest, null, '\t') + '\n');

// README catalog table between markers
const table = [
	'| Company | Category | Agents | Teams | Skills | |',
	'| --- | --- | --- | --- | --- | --- |',
	...companies.map(
		(c) =>
			`| [**${c.name}**](./companies/${c.slug}) — ${c.description} | ${c.category} | ${c.agents} | ${c.teams} | ${c.skills} | ${c.featured ? '⭐' : ''} |`,
	),
].join('\n');
const readmePath = join(ROOT, 'README.md');
const readme = readFileSync(readmePath, 'utf8');
writeFileSync(
	readmePath,
	readme.replace(
		/<!-- catalog:start[^>]*-->[\s\S]*?<!-- catalog:end -->/,
		`<!-- catalog:start — regenerated by scripts/build-manifest.mjs -->\n${table}\n<!-- catalog:end -->`,
	),
);
console.log(`✓ manifest.json + README updated (${companies.length} companies)`);
